        foreach ($statesWithLGAs as $stateData) {
            $state = State::updateOrCreate(
                ['code' => $stateData['code']],
                ['name' => $stateData['name']]
            );

            foreach ($stateData['lgas'] as $lgaName) {
                LocalGovernment::updateOrCreate(
                    ['state_id' => $state->id, 'name' => $lgaName]
                );
            }
        }

        $this->command->info('States and LGAs seeded successfully!');
